    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed
        String usuario = txtUsuario.getText();
        String contrasena = txtpass.getText();
        String rango = txtRango.getText();
        if(Usuario.identificarUsuarioNuevo(usuario)==-1){
        Usuario obj = new Usuario();
        obj.setNick(usuario);
        obj.setPassword(contrasena);
        obj.setRango(rango);
        ListaUsuario.agregar(obj);
        JOptionPane.showMessageDialog(this,"SE REGISTRO CORRECTAMENTE");
        this.dispose();
        }else{
            JOptionPane.showMessageDialog(this,"ESTE NOMBRE DE USUARIO NO ESTA DISPONIBLE");
        }
    }//GEN-LAST:event_btnRegistrarseActionPerformed

