    private final GregorianCalendar calendar = new GregorianCalendar();
    private final Hashtable<String, Cita> citas = new Hashtable<>();
    private final AgendaListModel alm = new AgendaListModel();
    
    private void update() {
        String meses[] = {"ENE", "FEB", "MAR", "ABRI", "MAYO", "JUN", "JUL", "AGO", "SEPT", "OCT", "NOV", "DIC"};
        int nYears = calendar.get(GregorianCalendar.YEAR);
        int nMes = calendar.get(GregorianCalendar.MONTH);
        int nDias = calendar.get(GregorianCalendar.DATE);
        txtFecha.setText(nDias + "/" + meses[nMes] + "/" + nYears);
        
        txtAno.setText("" + nYears);
        txtMes.setText("" + nMes);
        txtDia.setText("" + nDias);
        
        jLabel1.setText("citas:" + citas.size());
        alm.Limpiar();
        Enumeration<Cita> elementos = citas.elements();
        while (elementos.hasMoreElements()) {
            Cita element = elementos.nextElement();
            GregorianCalendar g = element.getCalendar();
            if (g.get(GregorianCalendar.YEAR) == calendar.get(GregorianCalendar.YEAR) && g.get(GregorianCalendar.MONTH) == calendar.get(GregorianCalendar.MONTH) && g.get(GregorianCalendar.DATE) == calendar.get(GregorianCalendar.DATE));
            alm.addcita(element);
            
        }
        
        jList1.setModel(alm);
        jList1.setCellRenderer(new DefaultListCellRenderer());
        
    }
    
    private void incDia(int n) {
        int nYears = calendar.get(GregorianCalendar.YEAR);
        int nMes = calendar.get(GregorianCalendar.MONTH);
        int nDias = calendar.get(GregorianCalendar.DATE) + n;
        calendar.set(nYears, nMes, nDias);
    }
